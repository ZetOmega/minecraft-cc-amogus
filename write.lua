function placingDown()
    local curSlot = 1

    while turtle.getItemCount(curSlot) == 0 and curSlot < 16 do
            curSlot = curSlot + 1
    end
    turtle.select(curSlot)
    turtle.placeDown()
end

function spaceToLetter()
    for i = 1, 2 do
        turtle.forward()
    end
    turtle.turnLeft()   
end


