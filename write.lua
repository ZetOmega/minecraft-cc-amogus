for k = 1,#letter do
    if     letter[k] == "a" then letterA()
    elseif letter[k] == "b" then letterB()
    elseif letter[k] == "c" then letterC()
    elseif letter[k] == "d" then letterD()
    elseif letter[k] == "e" then letterE()
    elseif letter[k] == "f" then letterF()
    elseif letter[k] == "g" then letterG()
    elseif letter[k] == "h" then letterH()
    elseif letter[k] == "i" then letterI()
    elseif letter[k] == "j" then letterJ()
    elseif letter[k] == "k" then letterK()
    elseif letter[k] == "l" then letterL()
    elseif letter[k] == "m" then letterM()
    elseif letter[k] == "n" then letterN()
    elseif letter[k] == "o" then letterO()
    elseif letter[k] == "p" then letterP()
    elseif letter[k] == "q" then letterQ()
    elseif letter[k] == "r" then letterR()
    elseif letter[k] == "s" then letterS()
    elseif letter[k] == "t" then letterT()
    elseif letter[k] == "u" then letterU()
    elseif letter[k] == "v" then letterV()
    elseif letter[k] == "w" then letterW()
    elseif letter[k] == "x" then letterX()
    elseif letter[k] == "y" then letterY()
    elseif letter[k] == "z" then letterZ()
    elseif letter[k] == "?" then letterQuestion()
    elseif letter[k] == "!" then letterExclamation()
    elseif letter[k] == "." then letterDot()
    elseif letter[k] == " " then letterSpace()
    else write("This is not a valid input")
end


end
